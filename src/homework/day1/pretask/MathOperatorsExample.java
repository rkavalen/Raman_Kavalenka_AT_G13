package homework.day1.pretask;

public class MathOperatorsExample {

    public static void main(String[] args){
        int a = 12;
        int b = 55;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + ((float)a / (float)b));   //      (float)(a / b) = 0.0
    }
}
