package homework.day1.pretask;

public class ConditionalExample {

    public static void main(String[] args) {
        int a = 6;

        if (a < 10) {
            System.out.println(a + " меньше десяти");
        } else if (a == 10) {
            System.out.println(a + " равно десяти");
        } else {
            System.out.println(a + " больше десяти");
        }
    }
}
